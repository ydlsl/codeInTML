class zhLineGroup extends Konva.Group{	

	constructor(config) {
		super(config);

		this.uniqueId = config.uniqueId
	}

	
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// ********** 节点周期函数扩展
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	destroy()
	{
		super.destroy();
	}
	
	UpdateGroup()
	{
		this.lineArray.forEach(line => {
			line.UpDateLine()
		});	
	}

	
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// ********** 绑定点信息
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// 绑定点信息
	TieWithPointGroup(pointGroup)
	{
		for (const index in this.lineArray) {
			
			let allPoints = pointGroup.GetPointsByIndex(index);
			this.lineArray[index].TieWithPoints(allPoints)
		}
	}



}

export default zhLineGroup;